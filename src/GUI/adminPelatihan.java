        java.util.Date tglSelesaiUtil = jDateChooser2.getDate();

        if(idPelatihan.isEmpty() || tglMulaiUtil == null || tglSelesaiUtil == null || namaPelatihan.isEmpty() || status.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        java.sql.Date tglMulai = new java.sql.Date(tglMulaiUtil.getTime());
        java.sql.Date tglSelesai = new java.sql.Date(tglSelesaiUtil.getTime());

        int konfirmasi = JOptionPane.showConfirmDialog(this, 
            "Apakah Anda yakin ingin memperbarui pelatihan ini?",
            "Konfirmasi Perbarui",
            JOptionPane.YES_NO_OPTION);

        if(konfirmasi != JOptionPane.YES_OPTION) return;

        try (Connection conn = koneksi.getConnection()) {
            PreparedStatement cekNama = conn.prepareStatement(
            "SELECT nama_pelatihan FROM pelatihan WHERE LOWER(nama_pelatihan) = LOWER(?) AND id_pelatihan <> ?");
            cekNama.setString(1, namaPelatihan);
            cekNama.setString(2, idPelatihan); 
            ResultSet rsNama = cekNama.executeQuery();

            if (rsNama.next()) {
                JOptionPane.showMessageDialog(this,
                    "Nama Pelatihan sudah terdaftar untuk ID lain!",
                    "Peringatan",
                    JOptionPane.WARNING_MESSAGE);
                return;
            }
            String sql = "UPDATE pelatihan SET tanggal_mulai=?, tanggal_selesai=?, nama_pelatihan=?, deskripsi=?, status_program=? WHERE id_pelatihan=?";
            PreparedStatement pst = conn.prepareStatement(sql);

            pst.setDate(1, tglMulai);
            pst.setDate(2, tglSelesai);
            pst.setString(3, namaPelatihan);
            pst.setString(4, deskripsi);
            pst.setString(5, status);
            pst.setString(6, idPelatihan);

            if(pst.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(this, "Pelatihan berhasil diperbarui!");
                loadDataPelatihan();
            } else {
                JOptionPane.showMessageDialog(this, "ID Pelatihan tidak ditemukan!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui pelatihan: " + e.getMessage());
        }

