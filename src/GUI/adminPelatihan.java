    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String idPelatihan = jTextField2.getText().trim();
        String namaPelatihan = jTextField4.getText().trim();
        String deskripsi = jTextField6.getText().trim();
        String status = jComboBox1.getSelectedItem().toString();

        java.util.Date tglMulaiUtil = jDateChooser1.getDate();
        java.util.Date tglSelesaiUtil = jDateChooser2.getDate();

        if(idPelatihan.isEmpty() || tglMulaiUtil == null || tglSelesaiUtil == null || 
           namaPelatihan.isEmpty() || status.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi kecuali deskripsi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if(!idPelatihan.startsWith("PL")) {
            JOptionPane.showMessageDialog(this, "ID Pelatihan harus diawali 'PL'!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        java.sql.Date tglMulai = new java.sql.Date(tglMulaiUtil.getTime());
        java.sql.Date tglSelesai = new java.sql.Date(tglSelesaiUtil.getTime());

        int konfirmasi = JOptionPane.showConfirmDialog(this, 
            "Apakah Anda yakin ingin menambahkan pelatihan ini?", 
            "Konfirmasi Tambah", 
            JOptionPane.YES_NO_OPTION);

        if(konfirmasi != JOptionPane.YES_OPTION) return;

        try (Connection conn = koneksi.getConnection()) {

            PreparedStatement cek = conn.prepareStatement("SELECT id_pelatihan FROM pelatihan WHERE id_pelatihan=?");
            cek.setString(1, idPelatihan);
            ResultSet rs = cek.executeQuery();
            if(rs.next()) {
                JOptionPane.showMessageDialog(this, "ID Pelatihan sudah ada!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }

            PreparedStatement pst = conn.prepareStatement(
                "INSERT INTO pelatihan(id_pelatihan, tanggal_mulai, tanggal_selesai, nama_pelatihan, deskripsi, status_program) VALUES(?,?,?,?,?,?)");

            pst.setString(1, idPelatihan);
            pst.setDate(2, tglMulai);
            pst.setDate(3, tglSelesai);
            pst.setString(4, namaPelatihan);
            pst.setString(5, deskripsi);
            pst.setString(6, status);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Pelatihan berhasil ditambahkan!");
            loadDataPelatihan();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menambahkan pelatihan: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

