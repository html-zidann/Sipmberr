        public void loadDataPelatihan() throws Exception {
            DefaultTableModel model = new DefaultTableModel(){
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };

            model.addColumn("ID Pelatihan");
            model.addColumn("Nama Pelatihan");
            model.addColumn("Deskripsi");
            model.addColumn("Tanggal Mulai");
            model.addColumn("Tanggal Selesai");
            model.addColumn("Status Program");


            pelatihanService service = new pelatihanService();
            List<pelatihan> daftar = service.getAllPelatihan();

            for (pelatihan p : daftar) {
                model.addRow(new Object[]{
                    p.getId(),
                    p.getNama(),
                    p.getDeskripsi(),
                    p.getTanggalMulai(),
                    p.getTanggalSelesai(),
                    p.getStatusProgram()
                });
            }

            jTable2.setModel(model);
        }
        
