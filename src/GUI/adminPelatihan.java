    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
     String idPelatihan = jTextField2.getText().trim();

    if(idPelatihan.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Pilih pelatihan yang akan dihapus!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int konfirmasi = JOptionPane.showConfirmDialog(this, 
        "Apakah Anda yakin ingin menghapus pelatihan ini?", 
        "Konfirmasi Hapus", 
        JOptionPane.YES_NO_OPTION);

    if(konfirmasi == JOptionPane.YES_OPTION) {
        try {
            Connection conn = koneksi.getConnection();
            String sql = "DELETE FROM pelatihan WHERE id_pelatihan=?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, idPelatihan);

            int deleted = pst.executeUpdate();
            if(deleted > 0) {
                JOptionPane.showMessageDialog(this, "Pelatihan berhasil dihapus!");
                loadDataPelatihan(); 
                
                jTextField2.setText("");
                jDateChooser1.setDate(null);
                jDateChooser2.setDate(null);
                jTextField4.setText("");
                jComboBox1.setSelectedIndex(0);
                jTextField6.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "ID Pelatihan tidak ditemukan!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            }

            pst.close();
            conn.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus pelatihan: " + e.getMessage());
        } catch (Exception e) {
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

