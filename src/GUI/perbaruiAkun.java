    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nama = naleng.getText().trim();
        String alamatText = alamat.getText().trim();
        String pekerjaanText = pekerjaan.getText().trim();
        String penghasilanText = penghasilan.getText().trim();
        String emailText = email.getText().trim();
        String noHp = nohp.getText().trim();
        String passwordText = password.getText().trim();
        String nik = nikPeserta; // dari konstruktor

        // --- VALIDASI ---
        String[] values = { nama, alamatText, pekerjaanText, penghasilanText, emailText, noHp, passwordText };
        String[] fieldNames = { "Nama", "Alamat", "Pekerjaan", "Penghasilan", "Email", "No HP", "Password" };

        for (int i = 0; i < values.length; i++) {
            if (values[i].isEmpty()) {
                JOptionPane.showMessageDialog(this, "Kolom " + fieldNames[i] + " wajib diisi!",
                        "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }

        if (!nama.matches("[a-zA-Z\\s]{5,}")) {
            JOptionPane.showMessageDialog(this, "Nama minimal 5 huruf dan hanya boleh huruf!",
                    "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (nik.length() != 16 || !nik.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "NIK harus terdiri dari tepat 16 digit angka!",
                    "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (!emailText.matches("^[A-Za-z0-9+_.-]+@(.+)$")) {
            JOptionPane.showMessageDialog(this, "Format email tidak valid!",
                    "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (!noHp.matches("\\d{11,}")) {
            JOptionPane.showMessageDialog(this, "No HP minimal 11 digit dan hanya angka!",
                    "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (!passwordText.matches("^(?=.*[A-Za-z])(?=.*\\d).{6,}$")) {
            JOptionPane.showMessageDialog(this, "Password minimal 6 karakter dan harus ada huruf serta angka!",
                    "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (!penghasilanText.matches("\\d+(\\.\\d{1,2})?")) {
            JOptionPane.showMessageDialog(this, "Penghasilan harus berupa angka!",
                    "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // --- Jika semua validasi lolos, baru lanjut UPDATE ---
        String sqlUpdate = "UPDATE pendaftar pd JOIN peserta p ON pd.nik=p.nik " +
                           "SET pd.nama=?, pd.alamat=?, pd.pekerjaan=?, pd.penghasilan=?, " +
                           "pd.email=?, pd.no_hp=?, pd.password=? WHERE p.nik=?";

        try (Connection conn = koneksi.getConnection();
             PreparedStatement pst = conn.prepareStatement(sqlUpdate)) {

            pst.setString(1, nama);
            pst.setString(2, alamatText);
            pst.setString(3, pekerjaanText);
            pst.setString(4, penghasilanText);
            pst.setString(5, emailText);
            pst.setString(6, noHp);
            pst.setString(7, passwordText);
            pst.setString(8, nik);

            int updated = pst.executeUpdate();
            if (updated > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
            } else {
                JOptionPane.showMessageDialog(this, "Tidak ada data yang diperbarui.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui data: " + e.getMessage());
        } catch (Exception ex) {
            System.getLogger(perbaruiAkun.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

