    private void loadDataPesertaLogin() throws Exception {
        String sql = "SELECT pd.nama, pd.alamat, pd.pekerjaan, pd.penghasilan, " +
             "pd.email, pd.no_hp, pd.password " +
             "FROM pendaftar pd JOIN peserta p ON pd.nik = p.nik " +
             "WHERE pd.nik = ?";

        try (Connection conn = koneksi.getConnection();
             PreparedStatement pst = conn.prepareStatement(sql)) {

            pst.setString(1, nikPeserta);
            try (ResultSet rs = pst.executeQuery()) {
                if (rs.next()) {
                    naleng.setText(rs.getString("nama"));
                    alamat.setText(rs.getString("alamat"));
                    pekerjaan.setText(rs.getString("pekerjaan"));
                    penghasilan.setText(rs.getString("penghasilan"));
                    email.setText(rs.getString("email"));
                    nohp.setText(rs.getString("no_hp"));
                    password.setText(rs.getString("password"));
                    NIK.setText(nikPeserta);
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data peserta: " + e.getMessage());
        }
    }
