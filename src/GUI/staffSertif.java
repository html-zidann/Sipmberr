        private void loadData() {
            sertifService srv = new sertifService();
            List<sertifikat> sertif = srv.getAllSertif();

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("NO Sertif");
            model.addColumn("Skor Pelatihan");
            model.addColumn("Tanggal terbit");
            model.addColumn("ID Peserta");

            for (sertifikat s : sertif) {
                double skor = s.getSkorPelatihan();
                System.out.println("Cek data: " + s.getNoSertif() + " | Skor: " + skor);

                if (Math.abs(skor) < 0.0001) {
                    model.addRow(new Object[]{
                        s.getNoSertif(),
                        String.format("%.2f", skor),
                        s.getTanggalTerbit(),
                        s.getIdPeserta()
                    });
                }
            }

    jTable1.setModel(model);
}
