    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Pilih data sertifikat dulu di tabel!");
        return;
    }

    String noSertif = jTable1.getValueAt(selectedRow, 0).toString();
    String skorText = jTextField1.getText().trim();

    if (skorText.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Masukkan nilai skor pelatihan terlebih dahulu!");
        return;
    }

    try {
        double skor = Double.parseDouble(skorText);

        if (skor < 0 || skor > 100) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nilai skor harus antara 0 hingga 100!");
            return;
        }

        sertifService srv = new sertifService();
        srv.updateSkorPelatihan(noSertif, skor);

        javax.swing.JOptionPane.showMessageDialog(this, "Skor pelatihan berhasil ditambahkan!");
        jTextField1.setText("");
        loadData();     

    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Nilai skor harus berupa angka!");
    }

    }//GEN-LAST:event_jButton1ActionPerformed

