    private void MasukBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MasukBtnActionPerformed
            String nik = NIK1.getText(); 
      String password = new String(jPasswordField1.getPassword());

    if(nik.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "NIK dan Password tidak boleh kosong!", "Login Gagal", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        Connection conn = koneksi.getConnection();

        String sqlPeserta = "SELECT status_verifikasi FROM pendaftar WHERE nik=? AND password=?";
        PreparedStatement pstPeserta = conn.prepareStatement(sqlPeserta);
        pstPeserta.setString(1, nik);
        pstPeserta.setString(2, password);
        ResultSet rsPeserta = pstPeserta.executeQuery();

        if(rsPeserta.next()) {
            String statusVerifikasi = rsPeserta.getString("status_verifikasi");

            switch(statusVerifikasi.toLowerCase()) {
                case "diterima" -> {
                    utamaPeserta formPeserta = new utamaPeserta(nik); 
                    formPeserta.setVisible(true);
                    this.dispose();
                    setLocationRelativeTo(null);
                }
                case "menunggu" -> JOptionPane.showMessageDialog(this, 
                        "Akun anda masih dalam tahap verifikasi oleh admin, coba lagi nanti",
                        "Verifikasi", JOptionPane.INFORMATION_MESSAGE);
                case "ditolak" -> JOptionPane.showMessageDialog(this, 
                        "Maaf akun anda tidak lolos tahap verifikasi, hubungi call center jika membutuhkan info lebih lanjut",
                        "Verifikasi", JOptionPane.ERROR_MESSAGE);
            }

        } else {
        String sqlStaff = "SELECT id_staff, nama, email, no_hp, password, jenis_staff FROM staff WHERE id_staff=? AND password=?";
            try (PreparedStatement pstStaff = conn.prepareStatement(sqlStaff)) {
                pstStaff.setString(1, nik);
                pstStaff.setString(2, password);
            try (ResultSet rsStaff = pstStaff.executeQuery()) {
                if(rsStaff.next()) {
                    String idStaff = rsStaff.getString("id_staff");
                    String nama = rsStaff.getString("nama");
                    String email = rsStaff.getString("email");
                    String nohp = rsStaff.getString("no_hp");
                    String pass = rsStaff.getString("password");
                    String jenis = rsStaff.getString("jenis_staff").trim();
                    
                    Model.roleStaff role;

                    if (jenis.equalsIgnoreCase("Admin")) {
                        role = new Model.admin(idStaff, nama, email, nohp, pass);
                        role.tugasUtama();
                        new adminPelatihan().setVisible(true);

                    } else if (jenis.equalsIgnoreCase("Staff Pelatihan")) {
                        role = new Model.staffPelatihan(idStaff, nama, email, nohp, pass);
                        role.tugasUtama();
                        new utamaStaff().setVisible(true);

                    } else {
                        JOptionPane.showMessageDialog(this, "Jenis staff tidak dikenali! (" + jenis + ")", "Error", JOptionPane.ERROR_MESSAGE);
                    }

                    
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "NIK / ID Staff atau Password salah!", "Login Gagal", JOptionPane.ERROR_MESSAGE);
                }
            }
            }
        }

        rsPeserta.close();
        pstPeserta.close();
        conn.close();

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_MasukBtnActionPerformed

