    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
    String nik = jTextField2.getText().trim();
    String nama = jTextField4.getText().trim();
    String alamat = jTextField6.getText().trim();
    String pekerjaan = jTextField1.getText().trim();
    String penghasilan = jTextField3.getText().trim();
    String email = jTextField5.getText().trim();
    String noHp = jTextField7.getText().trim();
    String password = jTextField8.getText().trim();
    String progress = jTextField9.getText().trim();

    String[] values = {nik, nama, alamat, pekerjaan, penghasilan, email, noHp, password, progress};
    String[] fieldNames = {"NIK", "Nama", "Alamat", "Pekerjaan", "Penghasilan", "Email", "No HP", "Password", "Progress"};

    for (int i = 0; i < values.length; i++) {
        if (values[i].isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kolom " + fieldNames[i] + " wajib diisi!", "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }

    if (!nama.matches("[a-zA-Z\\s]{5,}")) {
        JOptionPane.showMessageDialog(this, "Nama minimal 5 huruf dan hanya boleh huruf!", "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
        return;
    }
    if (!email.matches("^[A-Za-z0-9+_.-]+@(.+)$")) {
        JOptionPane.showMessageDialog(this, "Format email tidak valid!", "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
        return;
    }
    if (!noHp.matches("\\d{11,}")) {
        JOptionPane.showMessageDialog(this, "No HP minimal 11 digit dan hanya angka!", "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
        return;
    }
    if (!password.matches("^(?=.*[A-Za-z])(?=.*\\d).{6,}$")) {
        JOptionPane.showMessageDialog(this, "Password minimal 6 karakter dan harus ada huruf serta angka!", "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
        return;
    }
    if (!penghasilan.matches("\\d+(\\.\\d{1,2})?")) {
        JOptionPane.showMessageDialog(this, "Penghasilan harus berupa angka!", "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try (Connection conn = koneksi.getConnection()) {
        conn.setAutoCommit(false); 

        String sqlPendaftar = "UPDATE pendaftar SET nama=?, alamat=?, pekerjaan=?, penghasilan=?, email=?, no_hp=?, password=? WHERE nik=?";
        PreparedStatement pst1 = conn.prepareStatement(sqlPendaftar);
        pst1.setString(1, nama);
        pst1.setString(2, alamat);
        pst1.setString(3, pekerjaan);
        pst1.setString(4, penghasilan);
        pst1.setString(5, email);
        pst1.setString(6, noHp);
        pst1.setString(7, password);
        pst1.setString(8, nik);
        int updatedPendaftar = pst1.executeUpdate();
        pst1.close();


        String sqlPeserta = "UPDATE peserta SET progress=? WHERE nik=?";
        PreparedStatement pst2 = conn.prepareStatement(sqlPeserta);
        pst2.setString(1, progress);
        pst2.setString(2, nik);
        int updatedPeserta = pst2.executeUpdate();
        pst2.close();

        if (updatedPendaftar > 0 || updatedPeserta > 0) {
            conn.commit(); 
            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            loadDataPeserta(); 
        } else {
            conn.rollback();
            JOptionPane.showMessageDialog(this, "NIK tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

      
        if ("100%".equals(progress)) {
            try (Connection conn2 = koneksi.getConnection()) {
                PreparedStatement ps = conn2.prepareStatement(
                    "SELECT id_peserta FROM peserta WHERE nik = ?"
                );
                ps.setString(1, nik);
                ResultSet rs = ps.executeQuery();

                if (rs.next()) {
                    String idPeserta = rs.getString("id_peserta");
                    PreparedStatement cekSertifikat = conn2.prepareStatement(
                        "SELECT * FROM sertifikat WHERE id_peserta = ?"
                    );
                    cekSertifikat.setString(1, idPeserta);
                    ResultSet rsCek = cekSertifikat.executeQuery();

                    if (!rsCek.next()) {
                        String lastNumber = "SRT000";
                        Statement stmt = conn2.createStatement();
                        ResultSet rsLast = stmt.executeQuery(
                            "SELECT nomor_sertifikat FROM sertifikat ORDER BY nomor_sertifikat DESC LIMIT 1"
                        );
                        if (rsLast.next()) {
                            lastNumber = rsLast.getString("nomor_sertifikat");
                        }

                        int newNumber = Integer.parseInt(lastNumber.replace("SRT", "")) + 1;
                        String newNomor = String.format("SRT%03d", newNumber);

                        PreparedStatement insert = conn2.prepareStatement(
                            "INSERT INTO sertifikat (nomor_sertifikat, skor_pelatihan, tanggal_terbit, id_peserta) VALUES (?, ?, ?, ?)"
                        );
                        insert.setString(1, newNomor);
                        insert.setInt(2, 0);
                        insert.setDate(3, new java.sql.Date(System.currentTimeMillis()));
                        insert.setString(4, idPeserta);
                        insert.executeUpdate();

                        JOptionPane.showMessageDialog(this,
                            "Progress 100%! Sertifikat otomatis dibuat.\nNomor: " + newNomor,
                            "Sertifikat Dibuat", JOptionPane.INFORMATION_MESSAGE);
                    }
                    rsCek.close();
                    cekSertifikat.close();
                }
                rs.close();
                ps.close();

            } catch (SQLException ex2) {
                JOptionPane.showMessageDialog(this,
                    "Gagal membuat sertifikat otomatis: " + ex2.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
                ex2.printStackTrace();
            }
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton2ActionPerformed

