        private void loadDataPeserta() {
            pesertaService service = new pesertaService();
            List<peserta> daftarPeserta = service.getAllPeserta();

            DefaultTableModel model = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };

            // ðŸ”¹ Kolom sesuai tabel yang ditampilkan
            model.addColumn("NIK");
            model.addColumn("Nama");
            model.addColumn("Alamat");
            model.addColumn("Pekerjaan");
            model.addColumn("Penghasilan");
            model.addColumn("Email");
            model.addColumn("No HP");
            model.addColumn("Password");
            model.addColumn("Progress");

            try (Connection conn = koneksi.getConnection()) {
                for (peserta p : daftarPeserta) {
                    String sql = "SELECT * FROM pendaftar WHERE nik = ?";
                    PreparedStatement pst = conn.prepareStatement(sql);
                    pst.setString(1, p.getNik());
                    ResultSet rs = pst.executeQuery();

                    if (rs.next()) {
                        if (!"Diterima".equalsIgnoreCase(rs.getString("status_verifikasi"))) continue;

                        model.addRow(new Object[]{
                            rs.getString("nik"),
                            rs.getString("nama"),
                            rs.getString("alamat"),
                            rs.getString("pekerjaan"),
                            rs.getString("penghasilan"),
                            rs.getString("email"),
                            rs.getString("no_hp"),
                            rs.getString("password"),
                            p.getProgress() 
                        });
                    }
                }

                jTable2.setModel(model);

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error load peserta: " + e.getMessage());
            }
        }

